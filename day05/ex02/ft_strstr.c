char *ft_strstr(char *str, char *to_find)
{
	int i;
	i = 0;
	int j;

	while (str[i])
	{
   		j = 0;

		while (str[i + j] == to_find[j])
		{	
			j++;
		}
		if (!to_find[j])
			return (str + i);
		i++;
		}
		return (0);
}
